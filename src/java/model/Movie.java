public class Movie {

    private int no;
    private String name, url, room;

    public Movie(int no, String name, String url, String room) {
        this.no = no;
        this.name = name;
        this.url = url;
        this.room = room;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }

    @Override
    public String toString() {
        return "Movie{" + "no=" + no + ", name=" + name + ", url=" + url + ", room=" + room + '}';
    }

}
